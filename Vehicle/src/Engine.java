
public class Engine {
private String type;
private int hp;
private int torque;
private int mpg;

public Engine(String type, int hp, int torque, int mpg) {
	
}

public String getEngineType() {
	return type;
}

public int getHP() {
	return hp;
}

public int torque() {
	return torque;
}

public int mpg() {
	return mpg;
}
}
