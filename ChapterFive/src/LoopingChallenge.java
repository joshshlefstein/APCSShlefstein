
public class LoopingChallenge {

	public static void main(String[] args) {
		// Create a program that prints the sum from 1 to 1000
		// create a program that prints all the even numbers 1 to 100
		int z=0;
		for(int x=1; x<=1000; x++) {
			z=z+x;
			
		}
		System.out.println(z);
		
		for(int y=0; y<=100; y=y+2) {
			System.out.print(y+ " ");
		}

	}

}
